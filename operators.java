public class operators {
    public static void main(String[] args){
        // operators in java is a symbol which is used to perform operations

        int a = 1 + 2;
        // here 1, 2 are operands and '+' is the operator

        int myValue = 10;

        myValue = myValue + 10;
        //we can also write as myValue += 10;
        System.out.println(myValue);

        myValue = myValue - 10;
        // myValue -= 10;
        System.out.println(myValue);

        myValue = myValue * 10;
        //myValue *= 10;
        System.out.println(myValue);

        myValue = myValue / 10;
        //myValue /= 10;
        System.out.println(myValue);

        //remainder when 10 is divided by 3.
        myValue = myValue % 3;
        System.out.println(myValue);
    }
}
